#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *tmp)
{
	if (tmp)
	{
		if (!(*alst))
			*alst = tmp;
		else
		{
			tmp->next = *alst;
			*alst = tmp;
		}
	}
}
